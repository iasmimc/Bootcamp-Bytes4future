// Cria uma função que recebe duas diagonais de um losango
// como argumentos e calcula o perímetro dessa figura geométrica.
// Utiliza o que aprendeste sobre  para resolver este exercício.

function perimetroDoLosango(diagonalMaior, diagonalMenor) {
  // escreve aqui a função
  return 2 * Math.sqrt(diagonalMaior ** 2 + diagonalMenor ** 2);
}
